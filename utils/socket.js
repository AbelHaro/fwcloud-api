//create object
var socketTools = {};
var socket;
//Export the object
module.exports = socketTools;

/*----------------------------------------------------------------------------------------------------------------------*/
socketTools.init = req => {
  if (req.body.socketid) socket = req.app.get('socketio').sockets.connected[req.body.socketid];
}
/*----------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------------------------------------------------*/
socketTools.msg = data => {
  if (socket) socket.emit('log:info',data);
}
/*----------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------------------------------------------------*/
socketTools.msgEnd = () => {
  if (socket) socket.emit('log:END');
}
/*----------------------------------------------------------------------------------------------------------------------*/
