//create object
var socketTools = {};
var socket;
//Export the object
module.exports = socketTools;

/*----------------------------------------------------------------------------------------------------------------------*/
socketTools.msg = (data) => {
  if (socket) socket.emit('log:info',data);
}
/*----------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------------------------------------------------*/
socketTools.msgEnd = () => {
  if (socket) socket.emit('log:END');
}
/*----------------------------------------------------------------------------------------------------------------------*/
